import java.io.Serializable;

public class TeamPosition implements Serializable {
    private double x;
    private double y;

    public TeamPosition(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
